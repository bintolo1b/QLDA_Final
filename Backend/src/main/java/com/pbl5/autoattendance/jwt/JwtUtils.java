    @Value("${spring.app.jwtSecret}")
    private String jwtSecret;

    @Value("${spring.app.accessTokenExpirationMs}")
    private int accessJwtExpirationMs;

    @Value("${spring.app.refreshTokenExpirationMs}")
    private int refreshJwtExpirationMs;

